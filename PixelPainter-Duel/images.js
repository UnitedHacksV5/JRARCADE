// Function to get random image from pack
function getRandomImage(packName) {
    const pack = IMAGE_PACKS[packName];
    if (!pack || !pack.images.length) return null;
    
    const randomIndex = Math.floor(Math.random() * pack.images.length);
    return pack.images[randomIndex];
}

